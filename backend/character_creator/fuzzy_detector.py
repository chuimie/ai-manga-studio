def detect_fuzzy(text: str) -> dict | None:
    """
    检测输入文本中的模糊词
    返回第一个命中的模糊词信息，无命中返回None
    """
    for keyword, config in FUZZY_MAP.items():
        if keyword in text:
            return {
                "keyword": keyword,
                "question": config["question"],
                "options": config["options"]
            }
    return None